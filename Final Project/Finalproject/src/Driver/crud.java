    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Serialization
       
        try 
        {
            ObjectOutputStream oos=new ObjectOutputStream(new FileOutputStream("./Data/DayLightRecord.ser"));
            PersistantObject serializable=new PersistantObject(new Date(), dayLightList);
            oos.writeObject(serializable);
            serializeDate = new Date();
            JOptionPane.showMessageDialog(this,"Serialization was done succesfully at " + serializeDate+"The dayLightList is"+dayLightList.size());
        }
        catch (IOException ex)
        {
           // Logger.getLogger(Operations.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

