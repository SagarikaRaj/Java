    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Deserializing
         try 
         {
            ObjectInputStream ois=new ObjectInputStream(new FileInputStream("./Data/DayLightRecord.ser"));
            try 
            {
                deserializeDate = new Date(); 
                ois.readObject();
                JOptionPane.showMessageDialog(this,"Deserialization was done succesfully at" + deserializeDate);
                JOptionPane.showMessageDialog(this,"Time between serialization and deserialization is "+ (deserializeDate.getTime()-serializeDate.getTime())+" milliseconds");
            } 
            catch (ClassNotFoundException ex) 
            {
               // Logger.getLogger(Operations.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
         catch (IOException ex)
         {
          //  Logger.getLogger(Operations.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_jButton6ActionPerformed

